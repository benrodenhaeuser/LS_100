# minutes in a decade: How many minutes are there in decade?
# NOTE: I am disregarding leap-years here, there are three of those in a decade

years_in_a_decade = 10
days_in_a_year = 365
hours_in_a_day = 24
minutes_in_an_hour = 60

minutes_in_a_decade = years_in_a_decade * days_in_a_year * hours_in_a_day * minutes_in_an_hour

puts minutes_in_a_decade
# => 5.256.000
